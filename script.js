const questionEl = document.getElementById("question");
const answersEl = document.getElementById("answer-button");
const nextBtn = document.getElementById("next-btn");
const exitBtn = document.getElementById("exit-btn");
const timerEl = document.getElementById("timer");
const quizBox = document.querySelector(".quiz");
const selectionBox = document.querySelector(".quiz-selection");
const instructions = document.getElementById("instructions");
const luckText = document.getElementById("best-of-luck");


let questions = [];
let index = 0;
let score = 0;
let timeLeft = 10;
let timer = null;
let answered = false;


function startTimer() {
    clearInterval(timer);
    timeLeft = 10;
    timerEl.style.display = "block";
    timerEl.textContent = `Time Left: ${timeLeft}s`;

    timer = setInterval(() => {
        timeLeft--;
        timerEl.textContent = `Time Left: ${timeLeft}s`;

        if (timeLeft === 0) {
            clearInterval(timer);
            lockAnswers();
            nextBtn.style.display = "block";
        }
    }, 1000);
}

function stopTimer() {
    clearInterval(timer);
}



function startQuiz(selected) {
    questions = selected;
    index = 0;
    score = 0;

    selectionBox.style.display = "none";
    instructions.style.display = "none";
    luckText.style.display = "none";
    quizBox.style.display = "block";
    exitBtn.style.display = "block";

    showQuestion();
}

function showQuestion() {
    reset();
    answered = false;

    const q = questions[index];
    questionEl.textContent = `${index + 1}. ${q.question}`;

    q.answers.forEach(ans => {
        const btn = document.createElement("button");
        btn.textContent = ans.text;
        btn.className = "btn";
        btn.dataset.correct = ans.correct;
        btn.onclick = () => selectAnswer(btn);
        answersEl.appendChild(btn);
    });

    startTimer();
}

function reset() {
    stopTimer();
    nextBtn.style.display = "none";
    answersEl.innerHTML = "";
}



function selectAnswer(button) {
    if (answered) return;
    answered = true;

    if (button.dataset.correct === "true") {
        button.classList.add("correct");
        score++;
    } else {
        button.classList.add("incorrect");
    }

    lockAnswers();
    nextBtn.style.display = "block";
}

function lockAnswers() {
    [...answersEl.children].forEach(btn => {
        if (btn.dataset.correct === "true") btn.classList.add("correct");
        btn.disabled = true;
    });
}


nextBtn.onclick = () => {
    stopTimer();
    index++;

    if (index < questions.length) showQuestion();
    else showScore();
};

exitBtn.onclick = () => {
    stopTimer();
    showScore();
};

function showScore() {
    reset();
    timerEl.style.display = "none";
    questionEl.innerHTML = `ðŸŽ‰ You scored <strong>${score}</strong> out of <strong>${questions.length}</strong>`;
    nextBtn.textContent = "Play Again";
    nextBtn.style.display = "block";
    exitBtn.style.display = "none";

    nextBtn.onclick = () => location.reload();
}


document.getElementById("html-css-js-btn").onclick = () => startQuiz(questionsHTMLCSSJS);
document.getElementById("ict-btn").onclick = () => startQuiz(questionsICT);
document.getElementById("ai-btn").onclick = () => startQuiz(questionsAI);
